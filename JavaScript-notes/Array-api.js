let arr = [1, 2, 3]
let arr_new = ['a', 'b', 'c']

// 不改变原数组=======================================
// ------ join --------
console.log(arr.join(','))

// 改变原数组=========================================
// ------ 栈方法 先进先出 ------
arr.shift()
arr.unshift(1)
// ------ 队列方法   后进先出 ------
arr.push(4)
arr.pop()

// -------  从索引1开始删除  删除一个元素 ----------
arr.splice(1, 1)
// -------  从索引1开始删除  删除一个元素 并且用另个一个元素替换----------
arr.splice(1, 1, 3)
